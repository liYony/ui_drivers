void ssud_GC9A01_fill(rt_uint16_t x_start, rt_uint16_t y_start, rt_uint16_t x_end, rt_uint16_t y_end, void *pcolor)
{
	rt_uint8_t data[4];

	/*Column addresses*/
	ssud_spi_write_cmd(0x2A);				//0x2A
	data[0] = (x_start >> 8) & 0xFF;
	data[1] = x_start & 0xFF;
	data[2] = (x_end >> 8) & 0xFF;
	data[3] = x_end & 0xFF;
	ssud_spi_write_data(data, 4);

	/*Page addresses*/
	ssud_spi_write_cmd(0x2B);				//0x2B
	data[0] = (y_start >> 8) & 0xFF;
	data[1] = y_start & 0xFF;
	data[2] = (y_end >> 8) & 0xFF;
	data[3] = y_end & 0xFF;
	ssud_spi_write_data(data, 4);

	/*Memory write*/
	ssud_spi_write_cmd(0x2C);				//0x2C
	ssud_spi_write_color(x_start, y_start, x_end, y_end, pcolor);
}

void ssud_GC9A01_set_orientation(rt_uint8_t orientation)
{

    rt_uint8_t data[] = {0x08, 0xC8, 0x68, 0xA8}; ///ggggg
    ssud_spi_write_cmd(0x36);
    ssud_spi_write_data((void *) &data[orientation], 1);
}
